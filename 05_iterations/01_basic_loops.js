// FOR)------------------------------------------------------------------------
for (let i = 0; i <= 10; i++) {
  const element = i;
  console.log(element);
}

// WHILE)------------------------------------------------------------------------
let i = 0;
while (i <= 10) {
  console.log(i);
  i += 2;
}

// DO-WHILE)------------------------------------------------------------------------
let score = 1;
do {
  console.log(score);
  score++;
} while (score <= 10);